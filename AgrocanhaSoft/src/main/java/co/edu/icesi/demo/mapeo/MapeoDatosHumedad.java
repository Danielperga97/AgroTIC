package co.edu.icesi.demo.mapeo;

public class MapeoDatosHumedad {
	
	private String nombreGrupoHumedad, nombreNivelHumedad, nombreTipoPermeabilidad;

	public String getNombreGrupoHumedad() {
		return nombreGrupoHumedad;
	}

	public void setNombreGrupoHumedad(String nombreGrupoHumedad) {
		this.nombreGrupoHumedad = nombreGrupoHumedad;
	}

	public String getNombreNivelHumedad() {
		return nombreNivelHumedad;
	}

	public void setNombreNivelHumedad(String nombreNivelHumedad) {
		this.nombreNivelHumedad = nombreNivelHumedad;
	}

	public String getNombreTipoPermeabilidad() {
		return nombreTipoPermeabilidad;
	}

	public void setNombreTipoPermeabilidad(String nombreTipoPermeabilidad) {
		this.nombreTipoPermeabilidad = nombreTipoPermeabilidad;
	}
	

}
